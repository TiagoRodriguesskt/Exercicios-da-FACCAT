"""
Escreva um script que converta metros para centímetros e milímetros. Lembrando que 1 metro = 100 centímetros e 1 metro = 1000 milímetros.
"""

metros = float(input("Digite o valor em metors: "))
centimetros = metros * 100
milimetros = metros * 1000
print(f"{metros} metros é igual a {centimetros} centimetros.")
print(f"{metros} metros é igual a {milimetros} milimetros.")
